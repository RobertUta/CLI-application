import {
  listContacts,
  getContactById,
  removeContact,
  addContact,
} from "./contacts.js"; // Importing functions from contacts.js

import { Command } from "commander"; // Importing Command class from commander module
const program = new Command(); // Creating a new Command instance
program
  .option("-a, --action <type>", "choose action")
  .option("-i, --id <type>", "user id")
  .option("-n, --name <type>", "user name")
  .option("-e, --email <type>", "user email")
  .option("-p, --phone <type>", "user phone");

program.parse(process.argv);

const argv = program.opts(); // Parsing command line arguments

function invokeAction({ action, id, name, email, phone }) {
  switch (action) {
    case "list":
      listContacts();
      break;

    case "get":
      getContactById(id);
      break;

    case "add":
      addContact(name, email, phone);
      break;

    case "remove":
      removeContact(id);
      break;

    default:
      console.warn("\x1B[31m Unknown action type!");
  }
}

invokeAction(argv); // Invoking the appropriate action based on parsed arguments
